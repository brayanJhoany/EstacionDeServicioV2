package Surtidores;

/**
 *
 * @author braya
 */
public class Surtidor {
    int id, puerto;

    public Surtidor(int id, int puerto) {
        this.id = id;
        this.puerto = puerto;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPuerto() {
        return puerto;
    }

    public void setPuerto(int puerto) {
        this.puerto = puerto;
    }
    
    
    
    
}
